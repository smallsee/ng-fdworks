  var newHover = true;
  var newPass = true;
  var fa = $('#lotest-news');
  var authorTab = $('.news-message-box>div');
  var authorNum = 0;
  var authorzIndex = 5;
  var authorTabLength = authorTab.length;
  authorTab.eq(authorNum).addClass('active').css({
    zIndex: authorzIndex++
  });


  if (newHover) {
    fa.on('mouseover', function () {
      clearInterval(proTimer);
    });
    fa.on('mouseout', function () {
      newPass = true;
      clearInterval(proTimer);
      proTime();

    })
  }

  function authorChanger() {
    $('.nivo-control').each(function (index, val) {
      if ($(val).attr('class').indexOf('active') > 0) {
        authorNum = index;
      }
    });

    authorTab.each(function (index, val) {
      $(val).removeClass('active');
    });
    authorTab.eq(authorNum).addClass('active').css({
      zIndex: authorzIndex++
    });

  }

var proSpeed = 0;

  function proTime() {
    if (newPass) {
      newPass = false;
      window.proTimer = setInterval(function () {
        proSpeed++;
        radialObj.value(proSpeed);
        if (proSpeed == 100) {
          newPass = true;
          $('.nivo-nextNav').click()
        }
      }, 30)
    }
  }


  $('#slider').nivoSlider({
    controlNav: true,
    animSpeed: 500,
    manualAdvance: true,
    prevText: '',
    nextText: '',
    afterLoad: function () {
      proTime();
    },
    afterChange: function () {
      newHover = true;
      proSpeed = 0;
      radialObj.value(proSpeed);
      authorChanger();
      clearInterval(proTimer);
      proTime();
    },
    beforeChange: function () {

      newHover = false;
      clearInterval(proTimer);
    }
  });
